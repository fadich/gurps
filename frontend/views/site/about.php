                <p>
                    Персонаж имеет четыре базовых атрибута:
                </p>
                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;сила (англ. Strength, ST) — физическая сила персонажа, определяющая возможность поднимать, носить
