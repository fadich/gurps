                <p>
                    GURPS основывается на очках персонажа как основном элементе создания персонажа. Эти очки расходуются
                    на атрибуты, выбор преимуществ и недостатков, приобретение уровней навыков. Общее количество
                    начальных очков может варьироваться от 1 до бесконечности, в зависимости от пожеланий мастера игры.
                    Стандартное определение уровней очков (из GURPS Lite 4th edition):

                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;слабые персонажи (25 и менее очков)- маленькие дети, изможденные рабы, зомби и т.п. ;
                </p>
                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;средние персонажи (25-75 очков) — обычные люди, к.т. бухгалтера, таксисты и т.п. ;
                </p>
                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;персонажи выше среднего (75-100 очков) — атлеты, ученые, богатые дворяне и т.п. все кто обладают
                    неоспоримым преимуществом над «обычным» человеком;
                </p>
                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;выдающиеся персонажи (100—200 очков) «звездные» спортсмены, «крутые» копы и т.п. ;
                </p>
                <p>
                    -&nbsp;&nbsp;&nbsp;&nbsp;что-то большее чем жизнь (200—300 очков) главные герои фильмов про кун-фу (бегают по макушкам
                    деревьев и т.п.) и фантастики;
                </p>
                <p>
